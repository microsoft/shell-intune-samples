Function Get-BlockingReasons {
    param($ProfileV1)

    $blockingReasons = @()
    $profileType = $ProfileV1["@odata.type"]
    $isMacOSProfile = $profileType -eq "#microsoft.graph.depMacOSEnrollmentProfile"
    $isIOSProfile = $profileType -eq "#microsoft.graph.depIOSEnrollmentProfile"

    if ($isMacOSProfile) {
        if ($ProfileV1.enableAuthenticationViaCompanyPortal -eq $true) {
            $blockingReasons += "enableAuthenticationViaCompanyPortal is enabled (Company Portal authentication is not supported in ECv2)"
        }
    }
    elseif ($isIOSProfile) {
        if ($ProfileV1.enableSingleAppEnrollmentMode -eq $true) {
            $blockingReasons += "enableSingleAppEnrollmentMode is enabled (single app enrollment mode is not supported in ECv2)"
        }
        $vppTokenId = $ProfileV1.companyPortalVppTokenId
        if (-not [string]::IsNullOrWhiteSpace($vppTokenId) -and $vppTokenId -ne "00000000-0000-0000-0000-000000000000") {
            $blockingReasons += "companyPortalVppTokenId is set (Company Portal VPP token is not supported in ECv2)"
        }
        if ($ProfileV1.enableAuthenticationViaCompanyPortal -eq $true) {
            $blockingReasons += "enableAuthenticationViaCompanyPortal is enabled (Company Portal authentication is not supported in ECv2)"
        }
        if ($ProfileV1.requireCompanyPortalOnSetupAssistantEnrolledDevices -eq $true -and $ProfileV1.configurationWebUrl -eq $false) {
            $blockingReasons += "Setup Assistant Legacy mode (requireCompanyPortalOnSetupAssistantEnrolledDevices without modern auth) is not supported in ECv2"
        }
    }
    else {
        $platform = Get-ProfilePlatform -ProfileV1 $ProfileV1
        $blockingReasons += "Platform '$platform' is not supported for ECv1 to ECv2 conversion"
    }

    return $blockingReasons
}

Function Convert-AndCreatePolicy {
    param(
        $ProfileV1,
        [string]$TokenId,
        [string]$TokenName
    )

    $v1Name = $ProfileV1.displayName
    $v1Id = $ProfileV1.id
    $platform = Get-ProfilePlatform -ProfileV1 $ProfileV1
    $deviceConfigurationName = "${V2PolicyPrefix} " + $v1Name

    # Step 1: Convert the profile to ECv2 JSON payload
    try {
        $dcv2Payload = Convert-EnrollmentProfile-DeviceConfigurationPolicyV2 -profileV1 $ProfileV1 -deviceConfigurationName $deviceConfigurationName
    }
    catch {
        Add-ConversionResult -TokenId $TokenId -TokenName $TokenName -V1ProfileName $v1Name -V1ProfileId $v1Id `
            -Platform $platform -Status "Failed" -FailureReason "Conversion error: $($_.Exception.Message)"
        return
    }

    # If Convert returned null, the profile has non-convertible settings
    if ($null -eq $dcv2Payload -or $dcv2Payload -eq "") {
        $blockingReasons = Get-BlockingReasons -ProfileV1 $ProfileV1
        $detailedReason = if ($blockingReasons.Count -gt 0) {
            "Non-convertible ECv1 settings: $($blockingReasons -join '; ')"
        } else {
            "Profile contains non-convertible ECv1 settings (conversion returned empty payload)"
        }
        Add-ConversionResult -TokenId $TokenId -TokenName $TokenName -V1ProfileName $v1Name -V1ProfileId $v1Id `
            -Platform $platform -Status "Skipped" -FailureReason $detailedReason
        return
    }

    # Step 2: Create the ECv2 policy via Graph API
    try {
        $DCP_resource = "deviceManagement/configurationPolicies"
        $uri = "$script:GraphBaseUrl/$($DCP_resource)"
        $dcv2Result = Invoke-MGGraphRequest -Uri $uri -Method POST -Body $dcv2Payload -ContentType "application/json"
        $v2Name = $dcv2Result.name
        $v2Id = $dcv2Result.id
        Write-Host "Successfully created ECv2 policy: $v2Name (Id: $v2Id) from ECv1 profile: $v1Name" -ForegroundColor Green

        Add-ConversionResult -TokenId $TokenId -TokenName $TokenName -V1ProfileName $v1Name -V1ProfileId $v1Id `
            -Platform $platform -Status "Success" -V2PolicyName $v2Name -V2PolicyId $v2Id
    }
    catch {
        $errorMsg = $_.Exception.Message
        # Try to capture Graph API response body for debugging
        $graphResponse = ""
        try {
            if ($_.ErrorDetails -and $_.ErrorDetails.Message) {
                $graphResponse = $_.ErrorDetails.Message
            } elseif ($_.Exception.Response) {
                $responseStream = $_.Exception.Response.GetResponseStream()
                if ($responseStream) {
                    $reader = New-Object System.IO.StreamReader($responseStream)
                    $responseStream.Position = 0
                    $graphResponse = $reader.ReadToEnd()
                    $reader.Close()
                }
            }
        } catch { }

        $fullError = "Graph API error: $errorMsg"
        if (-not [string]::IsNullOrWhiteSpace($graphResponse)) {
            $fullError += "`n`nGraph API Response:`n$graphResponse"
        }
        Write-Host "Failed to create ECv2 policy from ECv1 profile: $v1Name, Id: $v1Id. Error: $errorMsg" -ForegroundColor Red

        Add-ConversionResult -TokenId $TokenId -TokenName $TokenName -V1ProfileName $v1Name -V1ProfileId $v1Id `
            -Platform $platform -Status "Failed" -FailureReason $fullError
    }
}

