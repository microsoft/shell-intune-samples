
<#
.SYNOPSIS
    Converts ECv1 enrollment profiles to ECv2 device configuration policies via Graph API.

.DESCRIPTION
    This script reads ECv1 (depIOSEnrollmentProfile / depMacOSEnrollmentProfile) profiles and
    converts them to ECv2 device configuration policies using the Graph API.
    Supports Commercial, Canary, USGov, USGovDoD, and China cloud environments.

    Users can choose to convert:
    1. A single ECv1 profile (by TokenId and ProfileId)
    2. All ECv1 profiles under a specific ADE token (by TokenId)
    3. All ECv1 profiles across all ADE tokens in the tenant

    Users can also clean up (delete) ECv2 policies previously created by this script
    by providing a conversion report HTML file with -CleanupFromReport.

    If -ConversionMode is not specified, an interactive menu is displayed.

.PARAMETER Environment
    The cloud environment to target. Defaults to "Commercial".
    Supported values: Commercial, Canary, USGov, USGovDoD, China.

.PARAMETER CanaryApiVersion
    The Canary API version path segment (e.g., "testprodbeta_Intune_SH").
    Required when Environment is "Canary".

.PARAMETER TenantName
    Optional. The Entra ID tenant name (domain, e.g. "contoso.onmicrosoft.com") or tenant ID (GUID)
    to authenticate against. When specified, the sign-in is directed to this specific tenant. If omitted,
    the default/home tenant for the signed-in account is used.

.PARAMETER ConversionMode
    Which conversion to perform. If omitted, an interactive menu is shown.
    - Single: Convert a single profile (requires -TokenId and -ProfileId)
    - Token:  Convert all profiles under a specific ADE token (requires -TokenId)
    - All:    Convert all profiles across all ADE tokens in the tenant

.PARAMETER TokenId
    The ADE token ID. Required for ConversionMode "Single" and "Token".

.PARAMETER ProfileId
    The enrollment profile ID. Required for ConversionMode "Single".

.PARAMETER OutputPath
    Optional path for the HTML conversion report. Defaults to the script directory.

.PARAMETER V2PolicyPrefix
    Prefix for converted ECv2 policy names. Defaults to "ECv2".
    The resulting policy name will be "<prefix> <ECv1 profile name>".

.PARAMETER DryRun
    When specified, performs a dry-run analysis without creating any ECv2 policies.
    Generates an HTML report showing which profiles can be converted and which cannot.

.PARAMETER CleanupFromReport
    Path to an HTML conversion report generated by this script.
    Parses the report for successfully created ECv2 policy IDs and deletes them
    from the tenant after user confirmation.

.EXAMPLE
    # Interactive menu (default)
    .\Convert-ECv1ToECv2.ps1

.EXAMPLE
    # Convert a single profile
    .\Convert-ECv1ToECv2.ps1 -ConversionMode Single -TokenId "abc-123" -ProfileId "def-456"

.EXAMPLE
    # Convert all profiles under a specific ADE token
    .\Convert-ECv1ToECv2.ps1 -ConversionMode Token -TokenId "abc-123"

.EXAMPLE
    # Convert all profiles across all ADE tokens
    .\Convert-ECv1ToECv2.ps1 -ConversionMode All

.EXAMPLE
    # Canary environment with interactive menu
    .\Convert-ECv1ToECv2.ps1 -Environment Canary -CanaryApiVersion "testprodbeta_Intune_SH"

.EXAMPLE
    # USGov environment, convert all
    .\Convert-ECv1ToECv2.ps1 -Environment USGov -ConversionMode All

.EXAMPLE
    # Authenticate against a specific tenant
    .\Convert-ECv1ToECv2.ps1 -TenantName "contoso.onmicrosoft.com" -ConversionMode All

.EXAMPLE
    # Custom ECv2 policy name prefix
    .\Convert-ECv1ToECv2.ps1 -ConversionMode All -V2PolicyPrefix "Converted"

.EXAMPLE
    # Dry-run analysis (no policies created)
    .\Convert-ECv1ToECv2.ps1 -ConversionMode All -DryRun

.EXAMPLE
    # Clean up ECv2 policies created by a previous conversion
    .\Convert-ECv1ToECv2.ps1 -CleanupFromReport "C:\reports\ECv1-to-ECv2-ConversionReport_20260609_103138.html"
#>

