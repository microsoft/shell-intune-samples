Function Convert-MacOSEnrollmentProfile-DeviceConfigurationPolicyV2(){
    [cmdletbinding()]
    param
    (
        $profileV1,
        $deviceConfigurationName
    )

    # Check for ECv1 settings that are not convertible to ECv2
    $blockingReasons = @()
    if ($profileV1.enableAuthenticationViaCompanyPortal -eq $true) {
        $blockingReasons += "enableAuthenticationViaCompanyPortal is enabled (Company Portal authentication is not supported in ECv2)"
    }
    if ($blockingReasons.Count -gt 0) {
        $profileName = $profileV1.displayName
        $profileId = $profileV1.id
        Write-Host "Cannot convert macOS enrollment profile to V2, profile name: $profileName, id: $profileId. Non-convertible settings: $($blockingReasons -join '; ')" -ForegroundColor Red
        return
    }

    $settings = @()

    foreach ($v1Key in $profileV1.Keys) {
        if (-not $macOSV1ToV2Mapping.ContainsKey($v1Key)) {
            continue
        }

        $v2Key = $macOSV1ToV2Mapping[$v1Key]
        $v1Value = $profileV1[$v1Key]

        if($v2Key.Contains("ade_setupassistant")){
            if($v2Key.EndsWith("_departmentphone")){
                $settings += New-ConfigurationSetting -SettingInstance (New-SimpleSettingInstance -SettingDefinitionId $v2Key -Value $(if($null -eq $profileV1.supportPhoneNumber){""}else{$profileV1.supportPhoneNumber}) -IncludeTemplateReferences)
            }
            elseif($v2Key.EndsWith("_department")){
                $settings += New-ConfigurationSetting -SettingInstance (New-SimpleSettingInstance -SettingDefinitionId $v2Key -Value $(if($null -eq $profileV1.supportDepartment){""}else{$profileV1.supportDepartment}) -IncludeTemplateReferences)
            }
            else{
                $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $v2Key -Value $(if ($v1Value -eq $true) { "${v2Key}_0" } else { "${v2Key}_1" }) -IncludeTemplateReferences)
            }
        }
        elseif($v2Key -eq "ade_lockedenrollment"){
            $lockEnrollment = if($profileV1.profileRemovalDisabled -eq $true) {"${v2Key}_1"}else{"${v2Key}_0"}
            $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $v2Key -Value $lockEnrollment -IncludeTemplateReferences)
        }
    }

    $macOSUserAffinityValue = if($profileV1.requiresUserAuthentication -eq $true){"${macOSUserAffinitySetting}_1"}else{"${macOSUserAffinitySetting}_0"}

    if ($macOSUserAffinityValue -eq "${macOSUserAffinitySetting}_1") {
        # User affinity enabled — authenticationmethod must be a child setting
        $macOSAuthenticationMethodValue = if($profileV1.requiresUserAuthentication -eq $true -and $profileV1.configurationWebUrl -eq $true){
            "${macOSAuthenticationMethod}_2"
        }
        else{
            "${macOSAuthenticationMethod}_2"
        }
        $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $macOSUserAffinitySetting -Value $macOSUserAffinityValue -Children @(
            (New-ChoiceSettingInstance -SettingDefinitionId $macOSAuthenticationMethod -Value $macOSAuthenticationMethodValue -IncludeTemplateReferences)
        ) -IncludeTemplateReferences)
    }
    else {
        $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $macOSUserAffinitySetting -Value $macOSUserAffinityValue -IncludeTemplateReferences)
    }

    $macOSAwaitConfigurationValue = if($profileV1.waitForDeviceConfiguredConfirmation -eq $true){"${macOSAwaitConfiguration}_1"}else{"${macOSAwaitConfiguration}_0"}

    if ($macOSAwaitConfigurationValue -eq "${macOSAwaitConfiguration}_1") {
        # awaitConfiguration=_1 requires createLocalAdmin as a child, which requires createLocalPrimary as its child

        # Build createLocalPrimary instance
        if($profileV1.skipPrimarySetupAccountCreation -eq $false){
            $createLocalPrimaryValue = if($profileV1.setPrimarySetupAccountAsRegularUser -eq $true){
                "ade_accountsettings_createlocalprimary_1"
            }
            else{
                "ade_accountsettings_createlocalprimary_2"
            }

            $prefillValue = if($profileV1.dontAutoPopulatePrimaryAccountInfo -eq $true){"ade_accountsettings_prefillaccountinfo_0"}else{"ade_accountsettings_prefillaccountinfo_1"}

            if ($prefillValue -eq "ade_accountsettings_prefillaccountinfo_1") {
                # prefillaccountinfo_1 requires all children: primaryaccountname, primaryaccountfullname, restrictediting
                $primaryName = if(-not [string]::IsNullOrWhiteSpace($profileV1.primaryAccountUserName)){ $profileV1.primaryAccountUserName } else { "" }
                $primaryFullName = if(-not [string]::IsNullOrWhiteSpace($profileV1.primaryAccountFullName)){ $profileV1.primaryAccountFullName } else { "" }

                $createLocalPrimaryChildren = @(
                    (New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_prefillaccountinfo" -Value $prefillValue -Children @(
                        (New-SimpleSettingInstance -SettingDefinitionId "ade_accountsettings_primaryaccountname" -Value $primaryName),
                        (New-SimpleSettingInstance -SettingDefinitionId "ade_accountsettings_primaryaccountfullname" -Value $primaryFullName),
                        (New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_restrictediting" -Value $(if($profileV1.enableRestrictEditing -eq $true){"ade_accountsettings_restrictediting_1"}else{"ade_accountsettings_restrictediting_0"}))
                    ))
                )
            }
            else {
                # prefillaccountinfo_0: no name/restrictediting children needed
                $createLocalPrimaryChildren = @(
                    (New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_prefillaccountinfo" -Value $prefillValue)
                )
            }

            $createLocalPrimaryInstance = New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_createlocalprimary" -Value $createLocalPrimaryValue -Children $createLocalPrimaryChildren -IncludeTemplateReferences
        }
        else{
            # Default: no primary account setup
            $createLocalPrimaryInstance = New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_createlocalprimary" -Value "ade_accountsettings_createlocalprimary_0" -IncludeTemplateReferences
        }

        # Build createLocalAdmin instance with createLocalPrimary as child
        if(-not [string]::IsNullOrWhiteSpace($profileV1.adminAccountUserName) -or -not [string]::IsNullOrWhiteSpace($profileV1.adminAccountFullName)){
            # createLocalAdmin_1 requires all children: adminaccountname, adminaccountfullname, hideusersgroups, adminaccountpasswordrotation, createlocalprimary
            $adminName = if(-not [string]::IsNullOrWhiteSpace($profileV1.adminAccountUserName)){ $profileV1.adminAccountUserName } else { "" }
            $adminFullName = if(-not [string]::IsNullOrWhiteSpace($profileV1.adminAccountFullName)){ $profileV1.adminAccountFullName } else { "" }

            $createLocalAdminChildren = @(
                (New-SimpleSettingInstance -SettingDefinitionId "ade_accountsettings_adminaccountname" -Value $adminName),
                (New-SimpleSettingInstance -SettingDefinitionId "ade_accountsettings_adminaccountfullname" -Value $adminFullName),
                (New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_hideusersgroups" -Value $(if($profileV1.hideAdminAccount -eq $true){"ade_accountsettings_hideusersgroups_1"}else{"ade_accountsettings_hideusersgroups_0"})),
                (New-SimpleSettingInstance -SettingDefinitionId "ade_accountsettings_adminaccountpasswordrotation" -Value $(
                    if($null -ne $profileV1.depProfileAdminAccountPasswordRotationSetting -and $null -ne $profileV1.depProfileAdminAccountPasswordRotationSetting.autoRotationPeriodInDays){
                        $profileV1.depProfileAdminAccountPasswordRotationSetting.autoRotationPeriodInDays
                    } else { 0 }
                ) -SettingValueODataType "#microsoft.graph.deviceManagementConfigurationIntegerSettingValue"),
                $createLocalPrimaryInstance
            )

            $createLocalAdminInstance = New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_createlocaladmin" -Value "ade_accountsettings_createlocaladmin_1" -Children $createLocalAdminChildren -IncludeTemplateReferences
        }
        else{
            # Default: no admin account, but createLocalPrimary is still a child
            $createLocalAdminInstance = New-ChoiceSettingInstance -SettingDefinitionId "ade_accountsettings_createlocaladmin" -Value "ade_accountsettings_createlocaladmin_0" -Children @($createLocalPrimaryInstance) -IncludeTemplateReferences
        }

        $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $macOSAwaitConfiguration -Value $macOSAwaitConfigurationValue -Children @($createLocalAdminInstance) -IncludeTemplateReferences)
    }
    else {
        # awaitConfiguration=_0: no children needed
        $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $macOSAwaitConfiguration -Value $macOSAwaitConfigurationValue -IncludeTemplateReferences)
    }

    # Handle enabledSkipKeys with macOS-specific template GUIDs (termsofaddress has different GUIDs in macOS vs iOS template)
    foreach($skipKey in $enabledSkipKeysMapping.Keys){
        $v2SettingId = $enabledSkipKeysMapping[$skipKey]
        $settingValue = if($null -ne $profileV1.enabledSkipKeys -and $profileV1.enabledSkipKeys.Contains($skipKey)){
            "${v2SettingId}_0"
        }
        else{
            "${v2SettingId}_1"
        }

        # macOS template has different GUIDs for termsofaddress than iOS
        $macOSSkipKeyTemplateOverrides = @{
            "ade_setupassistant_termsofaddress" = @{
                valueTemplateId = "df5a331d-7042-4abc-91f4-a37c82deaf08"
                instanceTemplateId = "21017ab9-73d3-45dd-b715-8742791a5d73"
            }
        }

        if ($macOSSkipKeyTemplateOverrides.ContainsKey($v2SettingId)) {
            $override = $macOSSkipKeyTemplateOverrides[$v2SettingId]
            $settings += @{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSetting"
                "settingInstance" = @{
                    "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingInstance"
                    "settingDefinitionId" = $v2SettingId
                    "choiceSettingValue" = @{
                        "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingValue"
                        "value" = $settingValue
                        "children" = @()
                        "settingValueTemplateReference" = @{
                            "settingValueTemplateId" = $override.valueTemplateId
                        }
                    }
                    "settingInstanceTemplateReference" = @{
                        "settingInstanceTemplateId" = $override.instanceTemplateId
                    }
                }
            }
        }
        else {
            $settings += New-ConfigurationSetting -SettingInstance (New-ChoiceSettingInstance -SettingDefinitionId $v2SettingId -Value $settingValue -IncludeTemplateReferences)
        }
    }

    # Add required new ECv2 Setup Assistant settings for macOS (default to Hide - no ECv1 equivalent)
    $newMacOSEcv2Settings = @(
        @{ id = "ade_setupassistant_appstore"; valueTemplateId = "7e8d9c4a-2f5b-4e3c-8d9a-6b7c3e5f2a1d"; instanceTemplateId = "4c8e7d9a-3f2b-4d1a-9c8e-5b7a6d3f2e1c" },
        @{ id = "ade_setupassistant_intelligence"; valueTemplateId = "d0c43d95-99c7-40ce-b3a6-33098215adc9"; instanceTemplateId = "cca42541-0513-41a6-ae74-e1fe878b0593" },
        @{ id = "ade_setupassistant_enablelockdownmode"; valueTemplateId = "181a5e6d-df5c-4016-b484-3130d54ee9ab"; instanceTemplateId = "4a9db745-821d-44a1-a26a-06f2c637d8b8" },
        @{ id = "ade_setupassistant_osshowcase"; valueTemplateId = "8c2d7f9e-4a3b-4e1c-9d8e-5b7a3c6f2d1e"; instanceTemplateId = "5d9c8e7a-3f2b-4d1a-8e9c-6b5a7d3f2e4c" },
        @{ id = "ade_setupassistant_appearance"; valueTemplateId = "87067516-5437-44ce-a3cc-cee7b6518b46"; instanceTemplateId = "61b602c2-86a3-4bca-82e5-cc7e14e17895" },
        @{ id = "ade_setupassistant_softwareupdate"; valueTemplateId = "d9630ff4-94a4-4525-b37a-7c233a23a2bd"; instanceTemplateId = "1ad911de-03ea-45a1-917e-adf9ececc09c" },
        @{ id = "ade_setupassistant_softwareupdatecompleted"; valueTemplateId = "80bdf240-ad52-45fb-9a58-c888d812e75a"; instanceTemplateId = "8afaa22c-513a-41b4-9b49-1c888a6f1c53" }
    )
    foreach ($newSetting in $newMacOSEcv2Settings) {
        $settings += @{
            "@odata.type" = "#microsoft.graph.deviceManagementConfigurationSetting"
            "settingInstance" = @{
                "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingInstance"
                "settingDefinitionId" = $newSetting.id
                "choiceSettingValue" = @{
                    "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingValue"
                    "value" = "$($newSetting.id)_0"
                    "children" = @()
                    "settingValueTemplateReference" = @{
                        "settingValueTemplateId" = $newSetting.valueTemplateId
                    }
                }
                "settingInstanceTemplateReference" = @{
                    "settingInstanceTemplateId" = $newSetting.instanceTemplateId
                }
            }
        }
    }

    $dcv2Payload = @{
        creationSource = "DepTokenId_"+ $profileV1.id.Split("_")[0]
        name = $deviceConfigurationName
        description = if($null -eq $profileV1.description){ ""}else{$profileV1.description}
        platforms = "macOS"
        technologies = "enrollment"
        roleScopeTagIds = @()
        settings = $settings
        templateReference = @{
            "templateId" = "2e29557d-70fc-405a-8082-d1e5b6be2b8c_1"
        }
    }

    if ($null -eq $dcv2Payload.name -or $dcv2Payload.name -eq '') {
        throw "Enrollment profile 'name' value cannot be  null or empty. "
    }

    return $dcv2Payload | ConvertTo-Json -Depth 30
}

