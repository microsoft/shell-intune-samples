# ==========================================
# Cleanup ECv2 Policies from Report
# ==========================================

Function Remove-ECv2PoliciesFromReport {
    param(
        [string]$ReportFilePath
    )

    if (-not (Test-Path $ReportFilePath)) {
        Write-Host "Error: Report file not found: $ReportFilePath" -ForegroundColor Red
        return
    }

    Write-Host ""
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  ECv2 Policy Cleanup from Report" -ForegroundColor Cyan
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "Reading conversion report: $ReportFilePath" -ForegroundColor Yellow

    $htmlContent = Get-Content $ReportFilePath -Raw

    # Parse rows with data-status="Success" and extract V2 policy name and ID
    # Split into table rows and process Success rows
    $rowPattern = '<tr\s+data-token="[^"]*"\s+data-status="Success"[^>]*>(.*?)</tr>'
    $rowMatches = [regex]::Matches($htmlContent, $rowPattern, [System.Text.RegularExpressions.RegexOptions]::Singleline)

    $policiesToDelete = @()
    foreach ($rowMatch in $rowMatches) {
        $rowContent = $rowMatch.Groups[1].Value
        # Extract all <td> contents
        $tdPattern = '<td[^>]*>(.*?)</td>'
        $tdMatches = [regex]::Matches($rowContent, $tdPattern, [System.Text.RegularExpressions.RegexOptions]::Singleline)
        # The 7th <td> (index 6) contains the V2 policy info: Name<br/><span class='id-text'>Id</span>
        if ($tdMatches.Count -ge 7) {
            $v2Cell = $tdMatches[6].Groups[1].Value
            $idPattern = "^(.*?)<br/><span class='id-text'>(.*?)</span>$"
            $idMatch = [regex]::Match($v2Cell, $idPattern)
            if ($idMatch.Success) {
                $policyName = [System.Net.WebUtility]::HtmlDecode($idMatch.Groups[1].Value)
                $policyId = [System.Net.WebUtility]::HtmlDecode($idMatch.Groups[2].Value)
                if (-not [string]::IsNullOrWhiteSpace($policyId)) {
                    $policiesToDelete += [PSCustomObject]@{
                        Name = $policyName
                        Id   = $policyId
                    }
                }
            }
        }
    }

    if ($policiesToDelete.Count -eq 0) {
        Write-Host ""
        Write-Host "No successfully created ECv2 policies found in the report." -ForegroundColor Yellow
        Write-Host "Nothing to clean up." -ForegroundColor Yellow
        return
    }

    Write-Host ""
    Write-Host "Found $($policiesToDelete.Count) ECv2 policies to delete:" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "  #  ECv2 Policy Name                              Policy ID" -ForegroundColor Cyan
    Write-Host "  -- ------------------------------------------------ ------------------------------------" -ForegroundColor DarkGray
    $index = 1
    foreach ($policy in $policiesToDelete) {
        $displayName = if ($policy.Name.Length -gt 48) { $policy.Name.Substring(0, 45) + "..." } else { $policy.Name }
        Write-Host "  $($index.ToString().PadLeft(2)) $($displayName.PadRight(48)) $($policy.Id)" -ForegroundColor White
        $index++
    }
    Write-Host ""

    $confirmation = Read-Host "Are you sure you want to delete all $($policiesToDelete.Count) ECv2 policies listed above? (yes/no)"
    if ($confirmation -ne "yes") {
        Write-Host ""
        Write-Host "Cleanup cancelled. No policies were deleted." -ForegroundColor Yellow
        return
    }

    Write-Host ""
    Write-Host "Deleting ECv2 policies..." -ForegroundColor Yellow

    $DCP_resource = "deviceManagement/configurationPolicies"
    $successCount = 0
    $failCount = 0
    $logEntries = @()
    $timestamp = Get-Date -Format "yyyyMMdd_HHmmss"
    $logDir = Split-Path $ReportFilePath -Parent
    if ([string]::IsNullOrWhiteSpace($logDir)) { $logDir = $PSScriptRoot }
    $logPath = Join-Path $logDir "ECv2-Cleanup-Errors_${timestamp}.log"

    foreach ($policy in $policiesToDelete) {
        try {
            $deleteUri = "$($script:GraphBaseUrl)/$DCP_resource('$($policy.Id)')"
            Write-Host "  Deleting: $($policy.Name) ($($policy.Id))..." -ForegroundColor Yellow -NoNewline
            Invoke-MgGraphRequest -Method DELETE -Uri $deleteUri | Out-Null
            Write-Host " Done" -ForegroundColor Green
            $successCount++
            $logEntries += "[SUCCESS] $($policy.Name) ($($policy.Id))"
        }
        catch {
            Write-Host " Failed" -ForegroundColor Red
            Write-Host "    Error: $($_.Exception.Message)" -ForegroundColor Red
            $failCount++

            # Capture detailed error info for the log
            $errorDetail = "[FAILED] $($policy.Name) ($($policy.Id))"
            $errorDetail += "`n  URI: DELETE $deleteUri"
            $errorDetail += "`n  Error: $($_.Exception.Message)"

            # Try to extract Graph API response body
            $responseBody = $null
            try {
                if ($_.ErrorDetails -and $_.ErrorDetails.Message) {
                    $responseBody = $_.ErrorDetails.Message
                }
                elseif ($_.Exception.Response) {
                    $stream = $_.Exception.Response.GetResponseStream()
                    if ($null -ne $stream) {
                        $reader = [System.IO.StreamReader]::new($stream)
                        $responseBody = $reader.ReadToEnd()
                        $reader.Dispose()
                    }
                }
            } catch { }

            if (-not [string]::IsNullOrWhiteSpace($responseBody)) {
                $errorDetail += "`n  Graph API Response:`n  $responseBody"
            }

            $errorDetail += "`n"
            $logEntries += $errorDetail
        }
    }

    # Write log file if there were any failures
    if ($failCount -gt 0) {
        $logHeader = "ECv2 Policy Cleanup Error Log"
        $logHeader += "`nTimestamp: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')"
        $logHeader += "`nReport:   $ReportFilePath"
        $logHeader += "`nTotal: $($policiesToDelete.Count) | Deleted: $successCount | Failed: $failCount"
        $logHeader += "`n$('=' * 80)`n"
        $logContent = $logHeader + ($logEntries -join "`n")
        $logContent | Out-File -FilePath $logPath -Encoding utf8
        Write-Host ""
        Write-Host "Error log saved to: $logPath" -ForegroundColor Yellow
    }

    Write-Host ""
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Cleanup Complete" -ForegroundColor Cyan
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Total:   $($policiesToDelete.Count)" -ForegroundColor White
    Write-Host "  Deleted: $successCount" -ForegroundColor Green
    if ($failCount -gt 0) {
        Write-Host "  Failed:  $failCount" -ForegroundColor Red
    }
    Write-Host ""
}

# ==========================================
# Handle CleanupFromReport mode (early exit)
# ==========================================

if ($PSBoundParameters.ContainsKey('CleanupFromReport') -and -not [string]::IsNullOrWhiteSpace($CleanupFromReport)) {
    Remove-ECv2PoliciesFromReport -ReportFilePath $CleanupFromReport
    return
}

# ==========================================
# Interactive Parameter Collection
# ==========================================

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  ECv1 to ECv2 Profile Conversion" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""

# 1. ConversionMode (default: All)
if (-not $PSBoundParameters.ContainsKey('ConversionMode')) {
    Write-Host "Select conversion mode:" -ForegroundColor Yellow
    Write-Host "  [1] Convert a single ECv1 profile (by ADE TokenId and ProfileId)"
    Write-Host "  [2] Convert all ECv1 profiles under a specific ADE token (by TokenId)"
    Write-Host "  [3] Convert all ECv1 profiles for all ADE tokens in the tenant (default)"
    Write-Host ""
    $selection = Read-Host "Enter selection (1-3, default: 3)"

    switch ($selection) {
        "1" { $ConversionMode = "Single" }
        "2" { $ConversionMode = "Token" }
        "3" { $ConversionMode = "All" }
        ""  { $ConversionMode = "All"; Write-Host "Using default: All" -ForegroundColor Gray }
        default {
            Write-Host "Invalid selection. Exiting." -ForegroundColor Red
            exit 1
        }
    }
}

# 2. TokenId (required for Single and Token modes)
if ($ConversionMode -in @("Single", "Token")) {
    if ([string]::IsNullOrWhiteSpace($TokenId)) {
        # Display available ADE tokens to help the user choose
        $availableTokens = @()
        try {
            $tokensResult = Get-AllTokens
            if ($tokensResult -and $tokensResult.value) {
                $availableTokens = @($tokensResult.value)
            }
        }
        catch {
            Write-Host "Warning: Could not retrieve ADE tokens: $($_.Exception.Message)" -ForegroundColor Yellow
        }

        if ($availableTokens.Count -gt 0) {
            Write-Host ""
            Write-Host "Available ADE tokens:" -ForegroundColor Yellow
            for ($i = 0; $i -lt $availableTokens.Count; $i++) {
                $t = $availableTokens[$i]
                $tokenName = if ([string]::IsNullOrWhiteSpace($t.tokenName)) { "(no name)" } else { $t.tokenName }
                Write-Host ("  [{0}] {1}  (Id: {2})" -f ($i + 1), $tokenName, $t.id)
            }
            Write-Host ""
            $tokenSelection = Read-Host "Enter ADE Token ID, or a number from the list above"

            if ($tokenSelection -match '^\d+$' -and [int]$tokenSelection -ge 1 -and [int]$tokenSelection -le $availableTokens.Count) {
                $TokenId = $availableTokens[[int]$tokenSelection - 1].id
                Write-Host "Selected token: $TokenId" -ForegroundColor Gray
            }
            else {
                $TokenId = $tokenSelection
            }
        }
        else {
            Write-Host "No ADE tokens were found to list." -ForegroundColor Yellow
            $TokenId = Read-Host "Enter ADE Token ID"
        }

        if ([string]::IsNullOrWhiteSpace($TokenId)) {
            Write-Host "ERROR: TokenId is required for ConversionMode '$ConversionMode'." -ForegroundColor Red
            exit 1
        }
    }
}

# 3. ProfileId (required for Single mode)
if ($ConversionMode -eq "Single") {
    if ([string]::IsNullOrWhiteSpace($ProfileId)) {
        $ProfileId = Read-Host "Enter Enrollment Profile ID"
        if ([string]::IsNullOrWhiteSpace($ProfileId)) {
            Write-Host "ERROR: ProfileId is required for ConversionMode 'Single'." -ForegroundColor Red
            exit 1
        }
    }
}

# 4. V2PolicyPrefix (default: ECv2)
if (-not $PSBoundParameters.ContainsKey('V2PolicyPrefix')) {
    $prefixInput = Read-Host "Enter ECv2 policy name prefix (default: ECv2)"
    if (-not [string]::IsNullOrWhiteSpace($prefixInput)) {
        $V2PolicyPrefix = $prefixInput
    }
    else {
        Write-Host "Using default prefix: ECv2" -ForegroundColor Gray
    }
}

# 5. DryRun (default: No)
if (-not $PSBoundParameters.ContainsKey('DryRun')) {
    $dryRunInput = Read-Host "Perform dry-run analysis only? (y/N, default: N)"
    if ($dryRunInput -eq "y" -or $dryRunInput -eq "Y") {
        $DryRun = [switch]::new($true)
        Write-Host "Dry-run mode enabled — no ECv2 policies will be created" -ForegroundColor Yellow
    }
    else {
        Write-Host "Using default: No (actual conversion)" -ForegroundColor Gray
    }
}

Write-Host ""
Write-Host "Configuration:" -ForegroundColor Cyan
Write-Host "  Conversion Mode: $ConversionMode" -ForegroundColor White
if ($ConversionMode -in @("Single", "Token")) { Write-Host "  Token ID:        $TokenId" -ForegroundColor White }
if ($ConversionMode -eq "Single") { Write-Host "  Profile ID:      $ProfileId" -ForegroundColor White }
Write-Host "  Policy Prefix:   $V2PolicyPrefix" -ForegroundColor White
Write-Host "  Dry Run:         $(if ($DryRun) { 'Yes' } else { 'No' })" -ForegroundColor $(if ($DryRun) { 'Yellow' } else { 'White' })

# ==========================================
# Execute Conversion or Dry-Run
# ==========================================

Write-Host ""

if ($DryRun) {
    Write-Host "Starting dry-run analysis (Mode: $ConversionMode)..." -ForegroundColor Yellow
    Write-Host "No ECv2 policies will be created." -ForegroundColor Yellow
    $script:DryRunResults = [System.Collections.ArrayList]::new()

    switch ($ConversionMode) {
        "Single" {
            Write-Host "Analyzing profile '$ProfileId' under token '$TokenId'..." -ForegroundColor Yellow
            $enrollmentProfile = Get-EnrollmentProfile -tokenId $TokenId -profileId $ProfileId
            $tokenInfo = try { Invoke-MGGraphRequest -Uri "$script:GraphBaseUrl/deviceManagement/depOnboardingSettings/$TokenId" -Method Get } catch { $null }
            $tokenName = if ($tokenInfo -and $tokenInfo.tokenName) { $tokenInfo.tokenName } else { $TokenId }
            Test-ProfileConvertibility -ProfileV1 $enrollmentProfile -TokenId $TokenId -TokenName $tokenName
        }
        "Token" {
            Write-Host "Analyzing all ECv1 profiles under token '$TokenId'..." -ForegroundColor Yellow
            $tokenInfo = try { Invoke-MGGraphRequest -Uri "$script:GraphBaseUrl/deviceManagement/depOnboardingSettings/$TokenId" -Method Get } catch { $null }
            $tokenName = if ($tokenInfo -and $tokenInfo.tokenName) { $tokenInfo.tokenName } else { $TokenId }
            $profiles = Get-AllEnrollmentProfileFromTokenId -tokenId $TokenId
            foreach ($profileV1 in $profiles.value) {
                if ($profileV1.id -like "ECV2_*") { continue }
                Test-ProfileConvertibility -ProfileV1 $profileV1 -TokenId $TokenId -TokenName $TokenId
            }
        }
        "All" {
            Write-Host "Analyzing all ECv1 profiles across all ADE tokens..." -ForegroundColor Yellow
            $allTokens = Get-AllTokens
            foreach ($token in $allTokens.value) {
                $currentTokenId = $token.id
                $currentTokenName = if ($token.tokenName) { $token.tokenName } else { $currentTokenId }
                Write-Host "  Token: $currentTokenName ($currentTokenId)..." -ForegroundColor Yellow
                $profiles = Get-AllEnrollmentProfileFromTokenId -tokenId $currentTokenId
                foreach ($profileV1 in $profiles.value) {
                    if ($profileV1.id -like "ECV2_*") { continue }
                    Test-ProfileConvertibility -ProfileV1 $profileV1 -TokenId $currentTokenId -TokenName $currentTokenName
                }
            }
        }
    }

    # Generate dry-run report
    $reportDir = if ($PSBoundParameters.ContainsKey('OutputPath') -and (Test-Path $OutputPath)) { $OutputPath } else { $PSScriptRoot }
    $reportFileName = "ECv1-to-ECv2-DryRunReport_$(Get-Date -Format 'yyyyMMdd_HHmmss').html"
    $reportPath = Join-Path $reportDir $reportFileName

    $generatedPath = New-DryRunReportHtml -ReportPath $reportPath -TenantInfo $tenantInfo -UserInfo $userInfo `
        -Environment $Environment -ConversionMode $ConversionMode -V2PolicyPrefix $V2PolicyPrefix -Results $script:DryRunResults

    $supportedCount = ($script:DryRunResults | Where-Object { $_.Status -eq "Supported" }).Count
    $notSupportedCount = ($script:DryRunResults | Where-Object { $_.Status -eq "Not Supported" }).Count

    Write-Host ""
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Dry-Run Analysis Complete" -ForegroundColor Cyan
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Total:         $($script:DryRunResults.Count)" -ForegroundColor White
    Write-Host "  Supported:     $supportedCount" -ForegroundColor Green
    Write-Host "  Not Supported: $notSupportedCount" -ForegroundColor Red
    Write-Host ""
    Write-Host "Report saved to: $generatedPath" -ForegroundColor Cyan
    Write-Host ""
}
else {
    Write-Host "Starting conversion (Mode: $ConversionMode)..." -ForegroundColor Cyan

    switch ($ConversionMode) {
        "Single" {
            Write-Host "Converting profile '$ProfileId' under token '$TokenId'..." -ForegroundColor Yellow
            $enrollmentProfile = Get-EnrollmentProfile -tokenId $TokenId -profileId $ProfileId
            $tokenInfo = try { Invoke-MGGraphRequest -Uri "$script:GraphBaseUrl/deviceManagement/depOnboardingSettings/$TokenId" -Method Get } catch { $null }
            $tokenName = if ($tokenInfo -and $tokenInfo.tokenName) { $tokenInfo.tokenName } else { $TokenId }
            Convert-AndCreatePolicy -ProfileV1 $enrollmentProfile -TokenId $TokenId -TokenName $tokenName
        }
        "Token" {
            Write-Host "Converting all ECv1 profiles under token '$TokenId'..." -ForegroundColor Yellow
            $tokenInfo = try { Invoke-MGGraphRequest -Uri "$script:GraphBaseUrl/deviceManagement/depOnboardingSettings/$TokenId" -Method Get } catch { $null }
            $tokenName = if ($tokenInfo -and $tokenInfo.tokenName) { $tokenInfo.tokenName } else { $TokenId }
            $profiles = Get-AllEnrollmentProfileFromTokenId -tokenId $TokenId
            foreach ($profileV1 in $profiles.value) {
                if ($profileV1.id -like "ECV2_*") { continue }
                Write-Host "  Converting: $($profileV1.displayName)..." -ForegroundColor Yellow
                Convert-AndCreatePolicy -ProfileV1 $profileV1 -TokenId $TokenId -TokenName $tokenName
            }
        }
        "All" {
            Write-Host "Converting all ECv1 profiles across all ADE tokens..." -ForegroundColor Yellow
            $allTokens = Get-AllTokens
            foreach ($token in $allTokens.value) {
                $currentTokenId = $token.id
                $currentTokenName = if ($token.tokenName) { $token.tokenName } else { $currentTokenId }
                Write-Host "  Processing token: $currentTokenName ($currentTokenId)..." -ForegroundColor Yellow
                $profiles = Get-AllEnrollmentProfileFromTokenId -tokenId $currentTokenId
                foreach ($profileV1 in $profiles.value) {
                    if ($profileV1.id -like "ECV2_*") { continue }
                    Write-Host "    Converting: $($profileV1.displayName)..." -ForegroundColor Yellow
                    Convert-AndCreatePolicy -ProfileV1 $profileV1 -TokenId $currentTokenId -TokenName $currentTokenName
                }
            }
        }
    }

    # Generate conversion report
    $reportDir = if ($PSBoundParameters.ContainsKey('OutputPath') -and (Test-Path $OutputPath)) { $OutputPath } else { $PSScriptRoot }
    $reportFileName = "ECv1-to-ECv2-ConversionReport_$(Get-Date -Format 'yyyyMMdd_HHmmss').html"
    $reportPath = Join-Path $reportDir $reportFileName

    $generatedPath = New-ConversionReportHtml -ReportPath $reportPath -TenantInfo $tenantInfo -UserInfo $userInfo `
        -Environment $Environment -ConversionMode $ConversionMode -Results $script:ConversionResults

    $successCount = ($script:ConversionResults | Where-Object { $_.Status -eq "Success" }).Count
    $failedCount = ($script:ConversionResults | Where-Object { $_.Status -eq "Failed" }).Count
    $skippedCount = ($script:ConversionResults | Where-Object { $_.Status -eq "Skipped" }).Count

    Write-Host ""
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Conversion Complete" -ForegroundColor Cyan
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "  Total:   $($script:ConversionResults.Count)" -ForegroundColor White
    Write-Host "  Success: $successCount" -ForegroundColor Green
    Write-Host "  Failed:  $failedCount" -ForegroundColor Red
    Write-Host "  Skipped: $skippedCount" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "Report saved to: $generatedPath" -ForegroundColor Cyan
    Write-Host ""
}
