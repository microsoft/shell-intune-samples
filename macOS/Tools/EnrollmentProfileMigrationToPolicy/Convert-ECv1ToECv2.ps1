 Function New-ChoiceSettingInstance(){
     param(
         [string]$SettingDefinitionId,
         [string]$Value,
         [array]$Children = @(),
         [switch]$IncludeTemplateReferences
     )

     $choiceSettingValue = @{
         "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingValue"
         "value" = $Value
         "children" = $Children
     }

     if($IncludeTemplateReferences -and $settingDefinitionIdToSettingValueTemplateId.ContainsKey($SettingDefinitionId)){
         $choiceSettingValue["settingValueTemplateReference"] = @{
             "settingValueTemplateId" = $settingDefinitionIdToSettingValueTemplateId[$SettingDefinitionId]
         }
     }

     $settingInstance = @{
         "@odata.type" = "#microsoft.graph.deviceManagementConfigurationChoiceSettingInstance"
         "settingDefinitionId" = $SettingDefinitionId
         "choiceSettingValue" = $choiceSettingValue
     }

